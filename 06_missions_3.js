
function march(girl)
{
    for (let jul = 0; jul <= girl; jul++)
    {
        if (jul === 5) {console.log ("five")}
        else if (jul === 13) {console.log ("thirteen")}
        else if (jul === 22) {console.log ("twenty two")}
        else if (jul === 35) {console.log ("thirty five")}
        else if (jul === 98) {console.log ("ninety eight")}
        console.log (jul)
    }    
}
console.log(march(100))